// Criando constantes
let opcao = 2

// Usando switch case
switch (opcao) {
    case 1:
        console.log("Opção 1")
        break
    case 2:
        console.log("Opção 2")
        break
    case 3:
        console.log("Opção 3")
        break
    default:
        console.log("Opção inválida")
        break
}